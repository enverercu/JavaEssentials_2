package Basic.Primitives;

public class EvenNumbers {

	public static void main(String args[])
    {
        int evenNumbers = 100;
                
        for (int i = 2; i <= evenNumbers; i ++){
            
            if (i % 2 == 0){
            
                System.out.println(i);
            }
        
        }
            
    }
}
